public class bitwise{
    public static void main(String[] args) {
        // BINARY AND - " & "
        System.out.println( 5 & 6 );

        // BINARY OR - " | "
        System.out.println( 5 | 6 );

        // BINARY XOR - " ^ "
        System.out.println( 5 ^ 6 );

        // BINARY ONE'S COMPLIMENT -  " ~ "
        System.out.println( ~ 5 );

        // BINARY LEFT SHIFT - " << "
        System.out.println( 5 << 2 );

        // BINARY RIGHT SHIFT - " >> "
        System.out.println( 6 >> 1 );
    }
}